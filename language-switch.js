// Get current language from localStorage or default to English
let currentLang = localStorage.getItem('language') || 'en';

// Function to update page content based on language
function updateLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('language', lang);
    
    const t = translations[lang];
    if (!t) return; // Exit if language not found
    
    // Update all elements with data-translate attribute
    Object.keys(t).forEach(key => {
        const elements = document.querySelectorAll(`[data-translate="${key}"]`);
        elements.forEach(el => {
            if (el.tagName === 'INPUT' && el.type === 'email') {
                el.placeholder = t[key];
            } else {
                el.textContent = t[key];
            }
        });
    });
}

// Initialize language on page load - handle both cases
function initializeLanguage() {
    updateLanguage(currentLang);
}

// Set up event listeners
function setupLanguageSwitchers() {
    const khmerItems = document.querySelectorAll('.dropdown-item.kh');
    const englishItems = document.querySelectorAll('.dropdown-item.us');
    
    khmerItems.forEach(item => {
        item.addEventListener('click', function(e) {
            e.preventDefault();
            updateLanguage('kh');
        });
    });
    
    englishItems.forEach(item => {
        item.addEventListener('click', function(e) {
            e.preventDefault();
            updateLanguage('en');
        });
    });
}

// Initialize on DOMContentLoaded
document.addEventListener('DOMContentLoaded', function() {
    initializeLanguage();
    setupLanguageSwitchers();
});

// Also initialize immediately in case DOM is already loaded
if (document.readyState === 'loading') {
    // DOM is still loading, wait for DOMContentLoaded (already set above)
} else {
    // DOM is already loaded (page was cached or script is loaded late)
    initializeLanguage();
    setupLanguageSwitchers();
}