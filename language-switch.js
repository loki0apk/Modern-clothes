// Get current language from localStorage or default to English
let currentLang = localStorage.getItem('language') || 'en';

// Function to update page content based on language
function updateLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('language', lang);
    
    const t = translations[lang];
    
    // Update all elements with data-translate attribute
    Object.keys(t).forEach(key => {
        const elements = document.querySelectorAll(`[data-translate="${key}"]`);
        elements.forEach(el => {
            if (el.tagName === 'INPUT' && el.type === 'email') {
                el.placeholder = t[key];
            } else {
                el.textContent = t[key];
            }
        });
    });
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
    updateLanguage(currentLang);
    
    // Add event listeners to language switcher
    document.querySelectorAll('.dropdown-item.kh').forEach(item => {
        item.addEventListener('click', function(e) {
            e.preventDefault();
            updateLanguage('kh');
            
            // Update dropdown text to Khmer
            const navLang = document.querySelector('[data-translate="navLanguage"]');
            if (navLang) navLang.textContent = 'ភាសាខ្មែរ';
        });
    });
    
    document.querySelectorAll('.dropdown-item.us').forEach(item => {
        item.addEventListener('click', function(e) {
            e.preventDefault();
            updateLanguage('en');
            
            // Update dropdown text to English
            const navLang = document.querySelector('[data-translate="navLanguage"]');
            if (navLang) navLang.textContent = 'English';
        });
    });
});
